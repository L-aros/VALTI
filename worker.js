const QUESTION_LOOKUP = new Map();

function registerQuestionLookup(question) {
  const aliases = [question.id, question.legacyId];

  for (const alias of aliases) {
    const existing = QUESTION_LOOKUP.get(alias);
    if (existing && existing.slug !== question.slug) {
      throw new Error(`题目标识冲突：${alias}`);
    }
    QUESTION_LOOKUP.set(alias, question);
  }
}

Object.values(NORMALIZED_QUESTION_BANK)
  .flat()
  .forEach(registerQuestionLookup);

