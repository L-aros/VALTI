function md5blk(input) {
  const blocks = [];
  for (let index = 0; index < 64; index += 4) {
    blocks[index >> 2] = input.charCodeAt(index) + (input.charCodeAt(index + 1) << 8) + (input.charCodeAt(index + 2) << 16) + (input.charCodeAt(index + 3) << 24);
  }
  return blocks;
}

function md51(input) {
  let index;
  const state = [1732584193, -271733879, -1732584194, 271733878];
  let text = input;

  for (index = 64; index <= text.length; index += 64) {
    md5cycle(state, md5blk(text.substring(index - 64, index)));
  }

  text = text.substring(index - 64);
  const tail = new Array(16).fill(0);

  for (index = 0; index < text.length; index += 1) {
    tail[index >> 2] |= text.charCodeAt(index) << ((index % 4) << 3);
  }

  tail[index >> 2] |= 0x80 << ((index % 4) << 3);

  if (index > 55) {
    md5cycle(state, tail);
    tail.fill(0);
  }

  tail[14] = input.length * 8;
  md5cycle(state, tail);
  return state;
}

function rhex(value) {
  const hexChars = "0123456789abcdef";
  let output = "";

  for (let index = 0; index < 4; index += 1) {
    output += hexChars.charAt((value >> (index * 8 + 4)) & 0x0f) + hexChars.charAt((value >> (index * 8)) & 0x0f);
  }

  return output;
}

function md5(input) {
  return md51(input).map(rhex).join("");
}

function makeOpaqueId(input) {
  let hash = 2166136261;
  for (let index = 0; index < input.length; index += 1) {
    hash ^= input.charCodeAt(index);
    hash = Math.imul(hash, 16777619);
  }
  return `q-${(hash >>> 0).toString(16).padStart(8, "0")}`;
}

