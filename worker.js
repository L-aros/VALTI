function ensureQuestionBankIntegrity() {
  const allQuestions = Object.values(NORMALIZED_QUESTION_BANK).flat();
  const slugSet = new Set();
  const md5Set = new Set();

  if (allQuestions.length !== 500) {
    throw new Error(`题库总数异常：期望 500，实际 ${allQuestions.length}`);
  }

  for (const slot of SLOT_ORDER) {
    const slotQuestions = NORMALIZED_QUESTION_BANK[slot];
    const expectedCount = SLOT_TOTALS[slot];

    if (!slotQuestions || slotQuestions.length !== expectedCount) {
      throw new Error(`题位数量异常：${slot} 期望 ${expectedCount}，实际 ${slotQuestions ? slotQuestions.length : 0}`);
    }

    for (const question of slotQuestions) {
      if (question.slot !== slot) {
        throw new Error(`题位不匹配：${question.slug}`);
      }

      if (slugSet.has(question.slug)) {
        throw new Error(`题目 slug 重复：${question.slug}`);
      }
      slugSet.add(question.slug);

      if (!/^[a-f0-9]{32}$/.test(question.id)) {
        throw new Error(`题目 MD5 非法：${question.slug}`);
      }

      if (md5Set.has(question.id)) {
        throw new Error(`题目 MD5 重复：${question.id}`);
      }
      md5Set.add(question.id);

      if (!Array.isArray(question.options) || question.options.length !== 3) {
        throw new Error(`题目选项数量异常：${question.slug}`);
      }

      if (WEIGHTED_SLOTS.has(slot) && question.weight !== 2) {
        throw new Error(`加权题缺少 weight: 2：${question.slug}`);
      }

      if (!WEIGHTED_SLOTS.has(slot) && typeof question.weight !== "undefined") {
        throw new Error(`非加权题不应携带 weight：${question.slug}`);
      }
    }
  }
}

