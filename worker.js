const rateLimitStore = new Map();

function parsePositiveInt(value) {
  if (typeof value !== "string") return null;
  const trimmed = value.trim();
  if (!trimmed) return null;
  const parsed = Number.parseInt(trimmed, 10);
  if (!Number.isInteger(parsed) || parsed <= 0) return null;
  return parsed;
}

function getRateLimitWindows(env) {
  const windowSeconds = parsePositiveInt(env?.RATE_LIMIT_WINDOW_SECONDS);
  const startLimit = parsePositiveInt(env?.RATE_LIMIT_START_LIMIT);
  const finishLimit = parsePositiveInt(env?.RATE_LIMIT_FINISH_LIMIT);

  return {
    start: windowSeconds && startLimit ? { limit: startLimit, windowSeconds } : null,
    finish: windowSeconds && finishLimit ? { limit: finishLimit, windowSeconds } : null
  };
}

function pickRandom(items) {
  return items[Math.floor(Math.random() * items.length)];
}

