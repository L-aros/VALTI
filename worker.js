function makeGeneratedQuestions(slot, blueprint, count) {
  const questions = [];
  const slotSlug = slot.toLowerCase().replace("_", "").toLowerCase();

  for (let contextIndex = 0; contextIndex < blueprint.contexts.length; contextIndex += 1) {
    for (let variantIndex = 0; variantIndex < blueprint.variants.length; variantIndex += 1) {
      if (questions.length >= count) break;

      const variant = blueprint.variants[variantIndex];
      questions.push({
        slug: `${slotSlug}-extra-c${String(contextIndex + 1).padStart(2, "0")}-v${variant.key}`,
        slot,
        ...(blueprint.weight ? { weight: blueprint.weight } : {}),
        text: `${blueprint.contexts[contextIndex]}${variant.tail}`,
        hint: variant.hint,
        art: variant.art,
        options: variant.options.map(option => ({ ...option }))
      });
    }
  }

  if (questions.length !== count) {
    throw new Error(`题库扩展题数量不足：${slot} 需要 ${count} 题，实际仅生成 ${questions.length} 题`);
  }

  return questions;
}

function buildQuestionBank() {
  return Object.fromEntries(
    SLOT_ORDER.map(slot => {
      const baseQuestions = BASE_QUESTION_BANK[slot] || [];
      const expectedCount = SLOT_TOTALS[slot];
      const extraCount = expectedCount - baseQuestions.length;
      const blueprint = EXPANSION_BLUEPRINTS[slot];

      if (!blueprint) {
        throw new Error(`缺少题库扩展蓝图：${slot}`);
      }

      const generatedQuestions = makeGeneratedQuestions(slot, blueprint, extraCount);
      return [slot, [...baseQuestions, ...generatedQuestions]];
    })
  );
}

function add32(a, b) {
  return (a + b) & 0xffffffff;
}

function cmn(q, a, b, x, s, t) {
  return add32(((add32(add32(a, q), add32(x, t)) << s) | (add32(add32(a, q), add32(x, t)) >>> (32 - s))), b);
}

function ff(a, b, c, d, x, s, t) {
  return cmn((b & c) | (~b & d), a, b, x, s, t);
}

function gg(a, b, c, d, x, s, t) {
  return cmn((b & d) | (c & ~d), a, b, x, s, t);
}

function hh(a, b, c, d, x, s, t) {
  return cmn(b ^ c ^ d, a, b, x, s, t);
}

function ii(a, b, c, d, x, s, t) {
  return cmn(c ^ (b | ~d), a, b, x, s, t);
}

